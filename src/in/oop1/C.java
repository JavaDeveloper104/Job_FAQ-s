package in.oop1;

public class C {

	public C() {
		System.out.println("class c zero param constructor");
	}
	public C(int a) {
		System.out.println("class c one param constructor value :"+a);
	}
	public C(int a,int b) {
		System.out.println("class c two param constructor values :"+(a+b));
	}
}
